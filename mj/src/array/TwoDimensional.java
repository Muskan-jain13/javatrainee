package array;

public class TwoDimensional {

	public static void main(String[] args) {
	
		int[][]values= {{1,2,3},{10,11,12},{20,21,22},{11,15,26}};
		for(int i=0;i<4;i++)
		{
			for(int j=0;j<3;j++)
			{
				System.out.println(values[i][j]+"");
			}
		System.out.println();
		}
		

	}

}
